#!/usr/bin/python
import math   # This will import math module

# Python number method ceil() returns ceiling value of x - the smallest integer not less than x.
print ("math.ceil(-45.17) : ", math.ceil(-45.17))
print ("math.ceil(100.12) : ", math.ceil(100.12))
print ("math.ceil(100.72) : ", math.ceil(100.72))
print ("math.ceil(119L) : ", math.ceil(119))
print ("math.ceil(math.pi) : ", math.ceil(math.pi))

# Python number method cmp() returns the sign of the difference of two numbers : -1 if x < y, 0 if x == y, or 1 if x > y.
#!/usr/bin/python

print ("cmp(80, 100) : ", math.cmp(80, 100))
print ("cmp(180, 100) : ", math.cmp(180, 100))
print ("cmp(-80, 100) : ", math.cmp(-80, 100))
print ("cmp(80, -100) : ", math.cmp(80, -100))

