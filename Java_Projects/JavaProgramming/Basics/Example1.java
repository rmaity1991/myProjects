package JavaProgramming.Basics;
/*
 * Java is a programming language and a platform. Java is a high level, robust,
 * object-oriented and secure programming language.
 * Any hardware or software environment in which a program runs, is known as a
 * platform. Since Java has a runtime environment (JRE) and API,
 * it is called a platform.
 * Types of Java Applications
 * There are mainly 4 types of applications that can be created using Java
 * programming:
 * 
 * 1) Standalone Application
 * Standalone applications are also known as desktop applications or
 * window-based applications. These are traditional software that we need
 * to install on every machine. Examples of standalone application are Media
 * player, antivirus, etc. AWT and Swing are used in Java for
 * creating standalone applications.
 * 
 * 2) Web Application
 * An application that runs on the server side and creates a dynamic page is
 * called a web application. Currently, Servlet, JSP, Struts,
 * Spring, Hibernate, JSF, etc. technologies are used for creating web
 * applications in Java.
 * 
 * 3) Enterprise Application
 * An application that is distributed in nature, such as banking applications,
 * etc. is called an enterprise application. It has advantages
 * like high-level security, load balancing, and clustering. In Java, EJB is
 * used for creating enterprise applications.
 * 
 * 4) Mobile Application
 * An application which is created for mobile devices is called a mobile
 * application. Currently, Android and Java ME are used for creating
 * mobile applications.
 */

